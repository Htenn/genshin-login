    <div class="box">
        <h3>
            Visions
        </h3>
        <p>
            Some beings in Teyvat are mysteriously granted Visions, which are external apparatuses that allow their owners to
            wield the elements. Visions take the form of crystals, each with the symbol and color of one element.
            <br><br>
            When an allogene dies, their Vision becomes a Masterless Vision, losing its power and all indicators of its element. Sometimes, Masterless Visions will resonate with a new person. For this reason, Masterless visions are a commodity sought by the extremely rich.
            <br><br>
            There are seven elements in Teyvat, each with a corresponding Archon and their own nation that worships said Archon. While Archons are powerful, it is unknown what the nature of the relationship between an Archon and their element is. Similarly, while regions are heavily affected by the element of their Archon, it is unknown what the nature of the relationship between a region and its element is.
        </p>
    </div>

